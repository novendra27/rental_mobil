    public void tampilkanData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Sewa");
        model.addColumn("Nama Mobil");
        model.addColumn("Tanggal Sewa");
        model.addColumn("Tanggal Pengembalian");
        model.addColumn("ID Mobil");
        ArraySewa arraySewa = mobilController.getArraySewa();
        for (int i = 0; i < arraySewa.getnElemen(); i++) {
            SewaModel sewa[] = arraySewa.getArrSewa();
            Object[] rowData = {sewa[i].getIDSewa(), sewa[i].getNamaMobil(), sewa[i].getTglSewa(),
                sewa[i].getTglPengembalian(), sewa[i].getIDMobil()};
            model.addRow(rowData);
        }
        tabelPengembalian.setModel(model);
    }

    private void centerWindow() {
        int screenWidth = java.awt.Toolkit.getDefaultToolkit().getScreenSize().width;
        int screenHeight = java.awt.Toolkit.getDefaultToolkit().getScreenSize().height;

        int frameWidth = getWidth();
        int frameHeight = getHeight();

        int x = (screenWidth - frameWidth) / 2;
        int y = (screenHeight - frameHeight) / 2;

        setLocation(x, y);
    }

