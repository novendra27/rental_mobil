    private void tabelPengembalianMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelPengembalianMouseClicked
        // TODO add your handling code here:
        int tabel = tabelPengembalian.getSelectedRow();
        txt_idMobil.setText(tabelPengembalian.getValueAt(tabel, 0).toString());
        txt_namaMobil.setText(tabelPengembalian.getValueAt(tabel, 1).toString());
        txt_tanggalPenyewaan.setText(tabelPengembalian.getValueAt(tabel, 2).toString());
        txt_tanggalPengembalian.setText(tabelPengembalian.getValueAt(tabel, 3).toString());
        Instant currentInstant = Instant.now();
        Timestamp sekarang = Timestamp.from(currentInstant);
        Timestamp tanggalPengembalian = Timestamp.valueOf(txt_tanggalPengembalian.getText());
        int perbandingan = sekarang.compareTo(tanggalPengembalian);
        if (perbandingan > 0) {
            txt_keterlambatan.setText("Terlambat");
        }else{
            txt_keterlambatan.setText("Belum Terlambat");
        }
    }//GEN-LAST:event_tabelPengembalianMouseClicked

