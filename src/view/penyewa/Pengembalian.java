    private void btn_kembalikanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_kembalikanActionPerformed
        boolean status = mobilController.setPengembalian(txt_idMobil.getText());
        if (status == true) {
            JOptionPane.showMessageDialog(null, "<html><b style=\"color:green; font-size:15px; "
                    + "background-color:white;\">Mobil Berhasil Dikembalikan<b></html>", "Pengembalian succes", 0, iconsucces);
            new Pengembalian().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "<html><b style=\"color:green; font-size:15px; "
                    + "background-color:white;\">Mobil Gagal Dikembalikan<b></html>" , "Pengembalian field", 0, iconfield);
            new Pengembalian().setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btn_kembalikanActionPerformed

